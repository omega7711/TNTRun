package fr.derycube.omega7711.TNTRun.utils;

public class Range {
    int from;
    int to;
    /**
     * Permet de créer une range d'un nombre à l'autre
     * @param from nombre le plus petit de la range
     * @param to nombre le plus grand
     */
    Range(int from, int to) {
        this.from = from;
        this.to = to;
    }
}
